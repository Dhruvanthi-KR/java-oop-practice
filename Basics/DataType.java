class DataType{
    public static void main(String[] args){
        int num = 24;
        long amount = 100000000L;
        double percent = 94.98;
        float pi = 3.14F;
        char sec = 'A';
        System.out.println("The integer value : " + num);
        System.out.println("The long value : " + amount);
        System.out.println("The float value : " + pi);
        System.out.println("The double value : " + percent);
        System.out.println("The character value : " + sec);
    }
}